
 function Showdiv() {
   var x = document.getElementById("event");
    if (x.style.display !== "block") {
      x.style.display = "block";
    }
 }

 function Hidediv() {
   var x = document.getElementById("event");
    if (x.style.display === "block") {
      x.style.display = "none";
    }
 }

